玩家2的牌組為<img height="15" src="//media.intellipoker.com/images/wrp-cards/cards/As.gif" width="25"><img height="15" src="//media.intellipoker.com/images/wrp-cards/cards/Jh.gif" width="25"><img height="15" src="//media.intellipoker.com/images/wrp-cards/cards/Tc.gif" width="25"><img height="15" src="//media.intellipoker.com/images/wrp-cards/cards/9d.gif" width="25"><img height="15" src="//media.intellipoker.com/images/wrp-cards/cards/6c.gif" width="25"><br>
