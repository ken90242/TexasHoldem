<h3>
  踢子</h3>
攤牌時，如果玩家的牌組大小相同，則由踢子決勝負。<br>
&nbsp;<br>
五張牌組成一副牌組，但只有順子、同花、葫蘆與同花大順會使用到五張牌。而遇到三條或兩對這種不需用全部牌組成的牌組時，其他的牌就有決定勝負的影響力了。散牌牌組最能說明此道理。
