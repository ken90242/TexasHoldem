公用牌是<img height="15" src="//media.intellipoker.com/images/wrp-cards/cards/9c.gif" width="25"><img height="15" src="//media.intellipoker.com/images/wrp-cards/cards/8c.gif" width="25"><img height="15" src="//media.intellipoker.com/images/wrp-cards/cards/9h.gif" width="25"><img height="15" src="//media.intellipoker.com/images/wrp-cards/cards/Ac.gif" width="25"><img height="15" src="//media.intellipoker.com/images/wrp-cards/cards/2h.gif" width="25"><br>
