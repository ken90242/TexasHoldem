<br>
假設公用牌中有是一對，而您擁有一張與該對子牌點相同的底牌，玩家的三條稱為「trips」，如底牌是<b><img height="15" src="//media.intellipoker.com/images/wrp-cards/cards/7x.gif" width="25"></b><b><img height="15" src="//media.intellipoker.com/images/wrp-cards/cards/2x.gif" width="25"></b>，公用牌是<img height="15" src="//media.intellipoker.com/images/wrp-cards/cards/2x.gif" width="25"><img height="15" src="//media.intellipoker.com/images/wrp-cards/cards/Jx.gif" width="25"><img height="15" src="//media.intellipoker.com/images/wrp-cards/cards/Ax.gif" width="25"><img height="15" src="//media.intellipoker.com/images/wrp-cards/cards/2x.gif" width="25"><img height="15" src="//media.intellipoker.com/images/wrp-cards/cards/8x.gif" width="25">。在實戰中，Set較trips有利，因為在trips的狀況下，對手也有可能組成三條，兩人靠踢子決勝，在set的狀況下，對手不可能組成同牌點的三條。<br>
