<h3>
  三條（分為set與trips）</h3>
<p>
  <img height="72" src="//media.intellipoker.com/images/wrp-cards/bigcards/9s.png" width="52"><img height="72" src="//media.intellipoker.com/images/wrp-cards/bigcards/9c.png" width="52"><img height="72" src="//media.intellipoker.com/images/wrp-cards/bigcards/9h.png" width="52"><img height="72" src="//media.intellipoker.com/images/wrp-cards/bigcards/Ks.png" width="52"><img height="72" src="//media.intellipoker.com/images/wrp-cards/bigcards/Ac.png" width="52"></p>
