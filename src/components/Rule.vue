<h3>
  同花</h3>
<p>
  <b><b><img height="72" src="//media.intellipoker.com/images/wrp-cards/bigcards/As.png" width="52"><img height="72" src="//media.intellipoker.com/images/wrp-cards/bigcards/Qs.png" width="52"><img height="72" src="//media.intellipoker.com/images/wrp-cards/bigcards/9s.png" width="52"><img height="72" src="//media.intellipoker.com/images/wrp-cards/bigcards/7s.png" width="52"><img height="72" src="//media.intellipoker.com/images/wrp-cards/bigcards/2s.png" width="52"></b></b></p>
同花是指五張不連續但花色相同的牌組（同花順較同花更強，後文將說明）。多位玩家同時握有同花時，花色不影響其牌組強弱，由牌點決定輸贏。<br>
&nbsp;
<ul>
  <li>
    <strong>最強同花：<br>
    </strong></li>
</ul>
假設您的底牌是<img height="15" src="//media.intellipoker.com/images/wrp-cards/cards/Ah.gif" width="25"><img height="15" src="//media.intellipoker.com/images/wrp-cards/cards/Kh.gif" width="25">，翻出的公用牌是<img height="15" src="//media.intellipoker.com/images/wrp-cards/cards/Qh.gif" width="25"><img height="15" src="//media.intellipoker.com/images/wrp-cards/cards/4h.gif" width="25"><img height="15" src="//media.intellipoker.com/images/wrp-cards/cards/Th.gif" width="25">，這表示您翻組成了同花，而且是最強的同花。<br>
&nbsp;<br>
如果兩位玩家同時握有組成同花，由牌點較大的玩家獲勝，如果兩位玩家的最大牌牌點相同，則比較次大牌，以此類推。
<ul>
  <li>
    <strong>同花對同花</strong></li>
</ul>
玩家1的底牌是<img height="15" src="//media.intellipoker.com/images/wrp-cards/cards/3s.gif" width="25"><img height="15" src="//media.intellipoker.com/images/wrp-cards/cards/8s.gif" width="25"><br>
玩家2的底牌是<img height="15" src="//media.intellipoker.com/images/wrp-cards/cards/Ad.gif" width="25"><img height="15" src="//media.intellipoker.com/images/wrp-cards/cards/9s.gif" width="25"><br>
公用牌是<img height="15" src="//media.intellipoker.com/images/wrp-cards/cards/As.gif" width="25"><img height="15" src="//media.intellipoker.com/images/wrp-cards/cards/Qs.gif" width="25"><img height="15" src="//media.intellipoker.com/images/wrp-cards/cards/Th.gif" width="25"><img height="15" src="//media.intellipoker.com/images/wrp-cards/cards/7s.gif" width="25"><img height="15" src="//media.intellipoker.com/images/wrp-cards/cards/2s.gif" width="25"><br>
