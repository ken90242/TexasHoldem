                                <h1><img src="//media.intellipoker.com/images_system/themes/nd2/bullett-arrow.gif" alt="">撲克牌組大小順序</h1>
                在牌桌上，只要能讓所有對手蓋牌，或是攤牌時手中的牌組優於其他玩家，就能獲勝。如何判斷哪位玩家的牌組最好呢？下文告訴您撲克牌組的大小順序。<br>
&nbsp;<br>
德州撲克比賽中，玩家從手上的2張底牌與桌面上的5張公用牌中，選出一組5張的最佳牌組；這5張牌中可以包含1張底牌、2張底牌，或是1張底牌也沒有。舉例來說，如果5張公用牌就是一副同花大順，那麼玩家能組成的最佳牌組就是一副同花大順，玩家手上的底牌沒辦法組成更佳的組合了（同花大順是德州撲克中最佳的牌組）。<br>
&nbsp;<br>
下列符號代表不同牌點的牌：<br>
<img height="15" src="//media.intellipoker.com/images/wrp-cards/cards/Ax.gif" width="25">代表A牌<br>
<img height="15" src="//media.intellipoker.com/images/wrp-cards/cards/Kx.gif" width="25">代表K牌<br>
<img height="15" src="//media.intellipoker.com/images/wrp-cards/cards/Qx.gif" width="25">代表Q牌<br>
<img height="15" src="//media.intellipoker.com/images/wrp-cards/cards/Jx.gif" width="25">代表J牌<br>
<img height="15" src="//media.intellipoker.com/images/wrp-cards/cards/Tx.gif" width="25">代表牌點為10的牌<br>
<img height="15" src="//media.intellipoker.com/images/wrp-cards/cards/9x.gif" width="25">代表牌點為9的牌<br>
數字代表牌上的點數，其餘牌點較小的牌皆可類推。<br>
<br>
下列符號代表各種花色的牌：<br>
<img height="15" src="//media.intellipoker.com/images/wrp-cards/cards/Xs.gif" width="25">代表黑桃<br>
<img height="15" src="//media.intellipoker.com/images/wrp-cards/cards/Xc.gif" width="25">代表梅花<br>
<img height="15" src="//media.intellipoker.com/images/wrp-cards/cards/Xh.gif" width="25">代表紅心<br>
<img height="15" src="//media.intellipoker.com/images/wrp-cards/cards/Xd.gif" width="25">代表方塊<br>
<br>
如果玩家的底牌都是兩張K（方塊K與梅花K），則表示為<img height="15" src="//media.intellipoker.com/images/wrp-cards/cards/Kd.gif" width="25"><img height="15" src="//media.intellipoker.com/images/wrp-cards/cards/Kc.gif" width="25">，若花色未知，則表示為<img height="15" src="//media.intellipoker.com/images/wrp-cards/cards/Kx.gif" width="25"><img height="15" src="//media.intellipoker.com/images/wrp-cards/cards/Kx.gif" width="25">。<br>
<br>
不同於許多其他紙牌遊戲，在撲克遊戲中，花色（梅花、黑桃、紅心、方塊）沒有高低之分。所以，玩家拿到梅花A或方塊A並沒有任何大小差別，只是花色不同，花色可用來組同花牌（見下圖）。攤牌時，如果多位玩家持有相同的牌組，僅花色不同，則其牌組大小相同，玩家將平分底池。<br>
&nbsp;<br>
<br>
<h2>
  撲克牌組大小順序</h2>
下列是撲克遊戲中可能出現的牌組，由弱到強排列，散牌最弱，同花大順最強。
<ol>
  <li>
    散牌</li>
  <li>
    一對</li>
  <li>
    兩對</li>
  <li>
    三條</li>
  <li>
    順子</li>
  <li>
    同花</li>
  <li>
    葫蘆</li>
  <li>
    四條</li>
  <li>
    同花順</li>
  <li>
    同花大順</li>
</ol>
<br>
<h3>
  牌組說明</h3>
撲克牌組共有10種。要看自己能組成什麼牌組時，請先記住「一副牌組一定包含5張牌」；在德州撲克中，最後會有7張可用的牌（2張底牌與5張公用牌），但您只能選擇5張組成最佳牌組。<br>
<br>
<br>
<h3>
  散牌</h3>
<p>
  <img height="72" src="//media.intellipoker.com/images/wrp-cards/bigcards/As.png" width="52"><img height="72" src="//media.intellipoker.com/images/wrp-cards/bigcards/4h.png" width="52"><img height="72" src="//media.intellipoker.com/images/wrp-cards/bigcards/Kc.png" width="52"><img height="72" src="//media.intellipoker.com/images/wrp-cards/bigcards/9s.png" width="52"><img height="72" src="//media.intellipoker.com/images/wrp-cards/bigcards/6h.png" width="52"></p>
如果玩家沒有任何特別牌組（一對，或更高的牌組），則手中或有的牌稱為散牌。
<ul>
  <li>
    <strong>散牌A<br>
    </strong></li>
</ul>
假設您的底牌是<img height="15" src="//media.intellipoker.com/images/wrp-cards/cards/Ax.gif" width="25"><img height="15" src="//media.intellipoker.com/images/wrp-cards/cards/6x.gif" width="25">，公用牌是<img height="15" src="//media.intellipoker.com/images/wrp-cards/cards/2x.gif" width="25"><img height="15" src="//media.intellipoker.com/images/wrp-cards/cards/5x.gif" width="25"><img height="15" src="//media.intellipoker.com/images/wrp-cards/cards/8x.gif" width="25">，這表示您拿到的是散牌，最好的單張牌是<img height="15" src="//media.intellipoker.com/images/wrp-cards/cards/Ax.gif" width="25">。若對手擁有<img height="15" src="//media.intellipoker.com/images/wrp-cards/cards/Kx.gif" width="25"><img height="15" src="//media.intellipoker.com/images/wrp-cards/cards/Qx.gif" width="25">，對手的牌也是散牌，最好的單張牌是<img height="15" src="//media.intellipoker.com/images/wrp-cards/cards/Kx.gif" width="25">。您的<img height="15" src="//media.intellipoker.com/images/wrp-cards/cards/Ax.gif" width="25">大於<img height="15" src="//media.intellipoker.com/images/wrp-cards/cards/Kx.gif" width="25">，因此由您獲勝。<br>
<br>
如果散牌中最大的單張牌牌點相同，則比較次大牌，如果次大牌仍相同，則以第三大的牌決勝負，以此類推。<br>
<ul>
  <li>
    <strong>散牌A，踢子K<br>
    </strong></li>
</ul>
玩家1的底牌是<img height="15" src="//media.intellipoker.com/images/wrp-cards/cards/As.gif" width="25"><img height="15" src="//media.intellipoker.com/images/wrp-cards/cards/Kc.gif" width="25"><br>
玩家2的底牌是<img height="15" src="//media.intellipoker.com/images/wrp-cards/cards/Ad.gif" width="25"><img height="15" src="//media.intellipoker.com/images/wrp-cards/cards/Qd.gif" width="25"><br>
公用牌是<img height="15" src="//media.intellipoker.com/images/wrp-cards/cards/9s.gif" width="25"><img height="15" src="//media.intellipoker.com/images/wrp-cards/cards/6h.gif" width="25"><img height="15" src="//media.intellipoker.com/images/wrp-cards/cards/4h.gif" width="25"><img height="15" src="//media.intellipoker.com/images/wrp-cards/cards/3s.gif" width="25"><img height="15" src="//media.intellipoker.com/images/wrp-cards/cards/2c.gif" width="25"><br>
<br>
兩位玩家的牌都是散牌，且最大牌都是A，但玩家1的次大牌（踢子）是K，大於玩家2的次大牌Q，因此由玩家1獲勝。<br>
<br>
<br>
<br>
<h3>
  一對</h3>
<p>
  <img height="72" src="//media.intellipoker.com/images/wrp-cards/bigcards/Ad.png" width="52"><img height="72" src="//media.intellipoker.com/images/wrp-cards/bigcards/Ac.png" width="52"><img height="72" src="//media.intellipoker.com/images/wrp-cards/bigcards/Ks.png" width="52"><img height="72" src="//media.intellipoker.com/images/wrp-cards/bigcards/Qc.png" width="52"><img height="72" src="//media.intellipoker.com/images/wrp-cards/bigcards/7d.png" width="52"></p>
玩家的底牌與公用牌中，兩張牌點相同的牌就是對子。<br>
<br>
假設您的底牌是<img height="15" src="//media.intellipoker.com/images/wrp-cards/cards/Ax.gif" width="25"><img height="15" src="//media.intellipoker.com/images/wrp-cards/cards/Kx.gif" width="25">，公用牌是<img height="15" src="//media.intellipoker.com/images/wrp-cards/cards/2x.gif" width="25"><img height="15" src="//media.intellipoker.com/images/wrp-cards/cards/7x.gif" width="25"><img height="15" src="//media.intellipoker.com/images/wrp-cards/cards/Kx.gif" width="25"><img height="15" src="//media.intellipoker.com/images/wrp-cards/cards/9x.gif" width="25"><img height="15" src="//media.intellipoker.com/images/wrp-cards/cards/Tx.gif" width="25">，則您有2張K，即一對K。如果您的對手也有一對K，則由踢子決勝負。另一種可能是，公用牌之中已有一個對子（例如<img height="15" src="//media.intellipoker.com/images/wrp-cards/cards/2x.gif" width="25"><img height="15" src="//media.intellipoker.com/images/wrp-cards/cards/Qx.gif" width="25"><img height="15" src="//media.intellipoker.com/images/wrp-cards/cards/Qx.gif" width="25"><img height="15" src="//media.intellipoker.com/images/wrp-cards/cards/7x.gif" width="25"><img height="15" src="//media.intellipoker.com/images/wrp-cards/cards/4x.gif" width="25">），此時牌局上未蓋牌的所有玩家都擁有這個對子，決勝負的關鍵在於玩家可否組出第二個對子或是更佳的牌組，否則即由踢子牌點最大的玩家獲勝。<br>
<ul>
  <li>
    <strong>一對A<br>
    </strong></li>
</ul>
玩家1的底牌是<img height="15" src="//media.intellipoker.com/images/wrp-cards/cards/Ad.gif" width="25"><img height="15" src="//media.intellipoker.com/images/wrp-cards/cards/Ks.gif" width="25"><br>
玩家2的底牌是<img height="15" src="//media.intellipoker.com/images/wrp-cards/cards/Qd.gif" width="25"><img height="15" src="//media.intellipoker.com/images/wrp-cards/cards/Js.gif" width="25"><br>
公用牌是<img height="15" src="//media.intellipoker.com/images/wrp-cards/cards/Ac.gif" width="25"><img height="15" src="//media.intellipoker.com/images/wrp-cards/cards/Qc.gif" width="25"><img height="15" src="//media.intellipoker.com/images/wrp-cards/cards/7d.gif" width="25"><img height="15" src="//media.intellipoker.com/images/wrp-cards/cards/5h.gif" width="25"><img height="15" src="//media.intellipoker.com/images/wrp-cards/cards/3s.gif" width="25"><br>
<br>
玩家1可用7張牌<img height="15" src="//media.intellipoker.com/images/wrp-cards/cards/Ad.gif" width="25"><img height="15" src="//media.intellipoker.com/images/wrp-cards/cards/Ks.gif" width="25"><img height="15" src="//media.intellipoker.com/images/wrp-cards/cards/Ac.gif" width="25"><img height="15" src="//media.intellipoker.com/images/wrp-cards/cards/Qc.gif" width="25"><img height="15" src="//media.intellipoker.com/images/wrp-cards/cards/7d.gif" width="25"><img height="15" src="//media.intellipoker.com/images/wrp-cards/cards/5h.gif" width="25"><img height="15" src="//media.intellipoker.com/images/wrp-cards/cards/3s.gif" width="25">，最佳牌組是一對A。玩家2可用的牌是<img height="15" src="//media.intellipoker.com/images/wrp-cards/cards/Qd.gif" width="25"><img height="15" src="//media.intellipoker.com/images/wrp-cards/cards/Js.gif" width="25"><img height="15" src="//media.intellipoker.com/images/wrp-cards/cards/Ac.gif" width="25"><img height="15" src="//media.intellipoker.com/images/wrp-cards/cards/Qc.gif" width="25"><img height="15" src="//media.intellipoker.com/images/wrp-cards/cards/7d.gif" width="25"><img height="15" src="//media.intellipoker.com/images/wrp-cards/cards/5h.gif" width="25"><img height="15" src="//media.intellipoker.com/images/wrp-cards/cards/3s.gif" width="25">，最佳牌組是一對Q，因此玩家1勝出。<br>
<br>
<br>
<h3>
  兩對</h3>
<p>
  <img height="72" src="//media.intellipoker.com/images/wrp-cards/bigcards/Kh.png" width="52"><img height="72" src="//media.intellipoker.com/images/wrp-cards/bigcards/Ks.png" width="52"><img height="72" src="//media.intellipoker.com/images/wrp-cards/bigcards/Qd.png" width="52"><img height="72" src="//media.intellipoker.com/images/wrp-cards/bigcards/Qs.png" width="52"><img height="72" src="//media.intellipoker.com/images/wrp-cards/bigcards/Td.png" width="52"></p>
玩家的底牌與公用牌中，兩個對子就稱為兩對。
<ul>
  <li>
